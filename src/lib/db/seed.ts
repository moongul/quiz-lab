async function seedTest(testData: TestData) {
  const { test, questions: qs, options: opts, results } = testData;

  await db.insert(tests).values({
    ...test,
    thumbnailUrl: null,
    totalParticipants: 0,
  }).onConflictDoNothing();

  for (const q of qs) {
    await db.insert(questions).values({ ...q, testId: test.id }).onConflictDoNothing();
  }

  for (let i = 0; i < opts.length; i++) {
    const opt = opts[i];
    await db.insert(options).values({
      id: `${test.id}-opt-${i}`,
      questionId: opt.questionId,
      content: opt.content,
      scores: JSON.stringify(opt.scores),
    }).onConflictDoNothing();
  }

  for (const result of results) {
    await db.insert(resultTypes).values({
      ...result,
      testId: test.id,
      imageUrl: null,
      count: 0,
    }).onConflictDoNothing();
  }

  console.log(`âœ… ${test.title} seeded!`);
}

export async function seedAllTests() {
  for (const testData of allTests) {
    await seedTest(testData);
  }
  console.log("\nğŸ‰ All tests seeded successfully!");
}
